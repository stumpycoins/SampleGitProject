package com.pointel;

public class MainController {

	private int addNumber(int a, int b) {
		return a + b;
	}

	private int subNumber(int a, int b) {
		return a - b;
	}

	private int multipleNumber(int a, int b) {
		return a * b;
	}

	private int divideNumber(int a, int b) {
		return a / b;
	}

	private int generateRandomNumber(int a, int b) {
		return a * b + a + b;
	}

}
